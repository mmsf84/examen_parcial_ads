package pruebas;

public class LibroElectronico {
	public String codLibro;
	public int indAtraso;
	public int indContieneDatos;
	public String tipoAtraso;
	public int indSimplificado;
	public String getCodLibro() {
		return codLibro;
	}
	public void setCodLibro(String codLibro) {
		this.codLibro = codLibro;
	}
	public int getIndAtraso() {
		return indAtraso;
	}
	public void setIndAtraso(int indAtraso) {
		this.indAtraso = indAtraso;
	}
	public int getIndContieneDatos() {
		return indContieneDatos;
	}
	public void setIndContieneDatos(int indContieneDatos) {
		this.indContieneDatos = indContieneDatos;
	}
	public String getTipoAtraso() {
		return tipoAtraso;
	}
	public void setTipoAtraso(String tipoAtraso) {
		this.tipoAtraso = tipoAtraso;
	}
	public int getIndSimplificado() {
		return indSimplificado;
	}
	public void setIndSimplificado(int indSimplificado) {
		this.indSimplificado = indSimplificado;
	}
	
	
}
